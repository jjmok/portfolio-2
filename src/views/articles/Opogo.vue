    <h2>Problem Statement</h2>
    <p> From the research, we decided to look into these 3 areas:</p>

    <h5 class="text-info">School needs to spend more time to look for supplies teacher with the right qualifications
    </h5>
    <blockquote>
      "If you're employing a secondary school teacher, you need to know what subjects they have taught and which top subjects they've specialised in."
    </blockquote>
    <ul>
      <li>
        In Opogo's case, teachers sign up to the Opogo Platform and go through the compliance process, the schools cover managers would employ teachers with the expertise they seek. However, there's no fast way to look for teacher with a specific set of qualifications on the Opogo App.
      </li>
    </ul>

    <h5 class="text-success">Supply Teachers travelling to new school feels anxious because there’s a lack of first day orientation
    </h5>

    <blockquote>
      “I always like to go to a school and feel like somewhat prepared, or know what I’m getting myself into.”
    </blockquote>
    <blockquote>
      “I had no idea where I was going. I didn’t even know where to put my bag.””
    </blockquote>
    <ul>
      <li>Due to the spontaneous nature of the job, supply teachers are sometimes called in to work on the morning without much notice. Teachers feels anxious on their first visit to a new school because there's a lack of information regarding these schools.</li>
      <li>
        On the Opogo app, Information regarding point of contact wasn't kept updated and there's overall lack of orientation for teachers on their first day.
      </li>
    </ul>

    <h5 class="text-warning">Supply teachers often feel left out of the loop and isolated due to the lack of an active community that keeps them engaged
    </h5>
    <blockquote>
      “I can’t find an obvious place within the app, to post and speak to people.”
    </blockquote>
    <ul>
      <li>Opogo has its own community platform and it mainly provides the outlet for teachers to share their learnings. Teachers often feel left out of the loop as there's no motivation to keep them engaged.</li>
    </ul>
