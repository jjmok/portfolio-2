    <h1 class="display-3">Improving the Job Search Experience for Supply Teachers</h1>
    <p>October 2020 - December 2020</p>

    <h2 class="display-4">Overview</h2>

    <p>In October 2020, I've joined an online UI/UX class organized by <a target=”_blank” href="https://experiencehaus.com/">Experience Haus</a> in the UK. As part of the course, I was provided the opportunity to work together with a real client, Opogo. They are a Social Enterprise that brings together technology and expertise to help support educators and schools.</p>

    <p>There are 12 participants and we are grouped into 4 teams. Each team will be carrying out their research and proposing their solutions</p>

    <h2 class="h4">A brief look at UK's education sector</h2>

    <blockquote class="blockquote bg-primary text-white p-4 italic">
      <em>"81% of educators are considering leaving the profession in the previous 12 months because of workload pressures"</em>
    </blockquote>

    <p>There is a very high demand for teachers in the UK and there are various recruitment agencies that serve to fill this gap. However, hiring teachers can be a very costly matter. Some schools had reportedly paid up to £11,000 in finder's fees to recruit teachers, making the hiring process very costly for both schools and educators. </p>

    <p>Teacher retention was poor. Educators had been leaving the profession due to workload pressure and a stressful working environment. At the time of writing, this situation had been exacerbated by the 2020 COVID pandemic. Overseas teachers can't travel abroad and this leads to a drop in potential teachers.</p>

    <p> Opogo is among one of the education platforms out there which aims not only to help schools save costs but to also find solutions to improve retention</p>
    
    <h2 class="h4">Users and Audience</h2>
    <p>Opogo's main user base is supply teachers and schools. While teachers use the Opogo app on mobile to look for jobs, the schools play a more active role in looking for teachers via the Desktop app.</p> 

    <p>Teachers firstly go through a compliance process in which they upload their credentials to be verified by the Opogo team. Verified teachers will be able to pick the time and date they are available for work and also set the distance they can travel. On the other end, Cover Managers will be able to scout, hire and manage their talents all on their talent pool in the desktop app.</p>

    <p>Apart from that, Opogo also has a thriving community of teachers who shares their learning and inspire each other. </p>

    <h2 class="h4">The Design Challenge</h2>
    
    <p>Opogo was interested in improving the user experience for their current app before developing the platform further. In the provided design brief, we were required to review the following flows:</p>
    <ul>
      <li>The onboarding flow for a new teacher when they sign up in the Opogo App</li>
      <li>the daily supply booking flow for the School Platform & the Opogo App </li>
    </ul>

    <p> While these are the main requests, we'll also be looking to point out other potential areas of improvement. We approach this challenge with the guidance of the Double Diamond Process</p>

